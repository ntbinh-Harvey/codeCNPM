package model;

import java.io.Serializable;

/**
 *
 * @author pc
 */
public class CarComponent implements Serializable{
    private int id;
    private String name;
    private int price;
    private String description;
    private String type;
    private int stock;

    public CarComponent() {
        super();
    }

    public CarComponent(String name, int price, String description, String type, int stock) {
        super();
        this.name = name;
        this.price = price;
        this.description = description;
        this.type = type;
        this.stock = stock;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getPrice() {
        return price;
    }

    public void setPrice(int price) {
        this.price = price;
    }

    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public int getStock() {
        return stock;
    }

    public void setStock(int stock) {
        this.stock = stock;
    }
}
